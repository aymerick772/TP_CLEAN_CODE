package ex1;

/**
 * Class Calcul
 */
public class Calcul {

    /**
     * Addition.
     *
     * @param a premier entier
     * @param b deuxième entier
     * @return la somme de a et b
     */
    public int addition(int a, int b) {
        return a + b;
    }

    /**
     * Soustraction.
     *
     * @param a premier entier
     * @param b deuxième entier
     * @return la différence entre a et b
     */
    public int soustraction(int a, int b) {
        return a - b;
    }
}
