package ex5;

public class Item {

    private String nom;
    private int poids;


    // Getter et setter pour le nom de l'item
    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getPoids() {
        return poids;
    }

    public void setPoids(int poids) {
        this.poids = poids;
    }
}
